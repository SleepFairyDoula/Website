		A doula (/ˈduːlə/) is a trained companion who is not a healthcare professional and who supports another individual (the doula's client) through a significant health-related experience, such as childbirth, miscarriage, induced abortion or stillbirth, or non-reproductive experiences such as dying. A doula may also provide support to the client's partner, family, and friends. The doula's goal, and role, is to help the client feel safe and comfortable, complementing the role of the healthcare professionals who provide the client's medical care.
		Unlike a physician, midwife, or nurse, a doula cannot administer medication or other medical treatment or give medical advice. An individual may need to complete training to work as a doula, although trainings and certification processes vary throughout the world. Some doulas work as volunteers; others are paid for their services by their client, medical institutions, or other private and public organizations. Doulas receive varying amounts of training, and their professionalization also varies. The contributions of doulas during reproductive experiences and end-of-life care have been studied and have been shown to benefit their clients. For example, a birth doula providing support during childbirth may increase likelihood of vaginal birth (rather than cesarean delivery), decrease the need for pain medication during labor, and improve the perception of the birthing experience. The benefits of a doula providing other types of support have been less well studied, but might improve a client's experience with medical care or help an individual cope with health transitions.
