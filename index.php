			I pushed for about 15-20 minutes and Joshua Samuel joined us at 12:21
			6 hours of labor naturally with no tearing, just like I prayed for OKAY GOD.
			That day goes down in the books for the most rewarding, joyful & exciting day of my life. I’m honestly still trying to find words for it. BUT the truth is, no matter what your story is, birthing your baby is to be celebrated and I hope you feel empowered to do ANYTHING.
			My hope in sharing this is that it encourages you pregnant mommas ... your body was made for this!</p>
